public class datatypes {
    public static void main(String[] args) {
        //data types 
        byte a = 12; // 1byte, -128 to 127
        short b = 100; // 2bytes, -32k to 32k 
        char c = 'z'; // 2bytes , can write only one charecter 
        boolean d = true; // 1 byte , only ture or false
        int e = 333333; // 4bytes , -2b to 2b
        float f = 23.24f; // 4bytes decimal values 
        long g = 63046451; // 8 bytes, same as int but large
        double h = 230.640; // 8 bytes , same as float but large
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);
        System.out.println(g);
        System.out.println(h);
    }
    
}
